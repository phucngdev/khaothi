const testOutlineSlice = createSlice({
  initialState,
  name: 'test_outline',
  reducers: {
    addOutline: (state, action: PayloadAction<any>) => {
      state.data.items.unshift(action.payload);
      state.data.meta.total = state.data.meta.total + 1;
    },

    updateOutline: (state, action: PayloadAction<any>) => {
      const index = state.data.items.findIndex(
        (item: any) => item.id === action.payload.id,
      );
      if (index !== -1) {
        state.data.items[index] = action.payload;
      }
    },

    deleteOutline: (state, action: PayloadAction<string>) => {
      state.data.items = state.data.items.filter(
        (item: any) => item.id !== action.payload,
      );
      state.data.meta.total = state.data.meta.total - 1;
    },

    editOutline: (state, action: PayloadAction<any>) => {
      state.dataEdit = action.payload;
    },

    createQuestion: (state, action: PayloadAction<any>) => {
      const { id_test_outline, id_matrix, data } = action.payload;
      state.data.items = state.data.items.map((item: any) =>
        item.id === id_test_outline
          ? {
              ...item,
              matrixs: item.matrixs.map((max: any) =>
                max.id === id_matrix
                  ? {
                      ...max,
                      questions: [data, ...max.questions],
                    }
                  : max,
              ),
            }
          : item,
      );
    },
  },
  extraReducers: builder => {
    builder;
  },
});

export default testOutlineSlice.reducer;
export const { addOutline, updateOutline, deleteOutline, editOutline } =
  testOutlineSlice.actions;
